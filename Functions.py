def suma(a,b):
    """
    :param a: ingresa un numero entero
    :param b: ingresa un numero enter
    :return:
    """
    r=a+b
    return r

def resta(a,b):
    """
        :param a: ingresa un numero entero
        :param b: ingresa un numero enter
        :return:
    """
    r=a-b
    return r

def multiplicar(a,b):
    """
        :param a: ingresa un numero entero
        :param b: ingresa un numero enter
        :return:
    """
    r=a*b
    return r

def dividir(a,b):
    """
        :param a: ingresa un numero entero
        :param b: ingresa un numero enter
        :return:
    """
    r=a/b
    return r


